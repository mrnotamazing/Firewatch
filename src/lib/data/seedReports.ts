const DAY = 24 * HOUR;
const now = Date.now();

const at = (agoMs: number, status: HazardReport['status']): Pick<HazardReport, 'createdAt' | 'dueAt' | 'resolvedAt'> => {
  const createdAt = now - agoMs;
  return {
    createdAt,
    dueAt: createdAt + 48 * HOUR,
    resolvedAt: status === 'resolved' ? createdAt + 31 * HOUR : undefined,
  };
};

